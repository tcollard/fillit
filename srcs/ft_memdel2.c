#include "../includes/fillit.h"

void	ft_memdel2(char **ap)
{
	size_t	x;

	x = 0;
	if (!ap || !(*ap))
		return ;
	while (ap[x])
	{
		free(ap[x]);
		x += 1;
	}
	free(ap);
	ap = 0;
}
